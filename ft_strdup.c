#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char			*str;
	unsigned int	i;

	if (!(str = (char *)malloc((ft_strlen(s1) + 1) * sizeof(char))))
		return (0);
	i = 0;
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
